// ─────────────────────────────────────────────────────────────────────────────
// TL;DR — auto-generated from data, falls back to REPLACE markers
// ─────────────────────────────────────────────────────────────────────────────
function buildTldr({ sports, markets, golf }) {
  const items = [];

  // Up to 2 sports bullets from game data
  (sports || []).slice(0, 2).forEach(g => {
    const w = g.home.winner ? g.home : g.away;
    const l = g.home.winner ? g.away : g.home;
    const note = g.note ? g.note.replace(/^NBA\s+/i, '') : g.shortName;
    items.push({
      tag: 'Sports', anchor: '#sports',
      html: `${esc(note)}: <strong>${esc(w.team)} ${esc(w.score)}</strong>, ${esc(l.team)} ${esc(l.score)}. <!-- REPLACE: Add player note (e.g. "Brunson 33 pts.") -->`,
    });
  });

  // Fill to 2 sports items if fewer than 2 games
  while (items.filter(i => i.tag === 'Sports').length < 2) {
    items.push({
      tag: 'Sports', anchor: '#sports',
      html: `REPLACE: Add sports headline. Format: "Series/matchup note. Player/stat. What's next."`,
    });
  }

  // Markets bullet — auto from Finnhub
  if (markets?.SPY?.dayChangePct !== null && markets?.SPY?.dayChangePct !== undefined) {
    const spy = markets.SPY;
    const dir = spy.dayChangePct >= 0 ? 'up' : 'down';
    items.push({
      tag: 'Markets', anchor: '#markets',
      html: `${tickerLink('SPY')} ${dir} ${fmtPct(spy.dayChangePct)} on the day. <!-- REPLACE: Add one key market note -->`,
    });
  } else {
    items.push({
      tag: 'Markets', anchor: '#markets',
      html: `REPLACE: Market headline. Format: "SPY/QQQ direction + %. Key story for next week."`,
    });
  }

  // Golf bullet — auto from ESPN data
  if (golf?.leaders?.[0]) {
    const l = golf.leaders[0];
    items.push({
      tag: 'Golf', anchor: '#golf',
      html: `${playerLink(l.name)} leads ${esc(golf.name)} at ${esc(l.score)}. <!-- REPLACE: Add context note -->`,
    });
  } else {
    items.push({
      tag: 'Golf', anchor: '#golf',
      html: `REPLACE: Golf headline. Format: "Player leads Tournament at score. What to watch."`,
    });
  }

  // Culture bullet — always manual
  items.push({
    tag: 'Culture', anchor: '#culture',
    html: `REPLACE: Culture headline. Format: "What happened. Why it matters." Max 20 words.`,
  });

  return `  <!-- TL;DR: Sports bullets auto-filled from ESPN data. Others need manual review. -->
  <div class="tldr">
    <div class="tldr-label">TL;DR — Five things to know</div>
    <ul class="tldr-list">
${items.slice(0, 5).map(item => `      <li class="tldr-item">
        <a href="${item.anchor}" class="tag-link"><span class="tag tag-amber">${item.tag}</span></a>
        <span>${item.html}</span>
      </li>`).join('\n')}
    </ul>
  </div>`;
}

// ─────────────────────────────────────────────────────────────────────────────
// Sports section — scoreboards + AI angle + REPLACE detail items
// ─────────────────────────────────────────────────────────────────────────────
function buildSports({ sports, copy }) {
  if (!sports?.length) {
    return `  <section class="brief-section" id="sports">
    <div class="section-label">Sports</div>
    <h3>REPLACE: Sports headline</h3>
    <!-- No game data found. Fill in scores and angles manually. -->
    <p>REPLACE: Add sports coverage. Scoreboard below can be filled in or removed.</p>
    <div class="scoreboard">
      <div class="score-side"><div class="score-team">TEAM A</div><div class="score-num">000</div></div>
      <div class="score-center"><div class="score-meta">REPLACE: Game label · Status</div><div class="score-badge">REPLACE Win</div></div>
      <div class="score-side right"><div class="score-team loser">TEAM B</div><div class="score-num loser">000</div></div>
    </div>
  </section>`;
  }

  const gameBlocks = sports.map((g, i) => {
    const w = g.home.winner ? g.home : g.away;
    const l = g.home.winner ? g.away : g.home;
    const homeLoser = !g.home.winner;
    const awayLoser = !g.away.winner;
    const metaText = g.note ? `${g.note} · ${g.status}` : g.status;
    const badge = `${esc(w.abbrev)} Win`;

    const scoreboard = `    <div class="scoreboard">
      <div class="score-side">
        <div class="score-team${homeLoser ? ' loser' : ''}">${esc(g.home.team)}</div>
        <div class="score-num${homeLoser ? ' loser' : ''}">${esc(g.home.score)}</div>
      </div>
      <div class="score-center">
        <div class="score-meta">${esc(metaText)}</div>
        <div class="score-badge">${badge}</div>
      </div>
      <div class="score-side right">
        <div class="score-team${awayLoser ? ' loser' : ''}">${esc(g.away.team)}</div>
        <div class="score-num${awayLoser ? ' loser' : ''}">${esc(g.away.score)}</div>
      </div>
    </div>`;

    // First game gets full treatment; subsequent get scoreboard + brief note
    if (i === 0) {
      const angleText = copy?.sportsAngle
        ? esc(copy.sportsAngle)
        : `REPLACE: Game analysis. Who was the key player? What was the turning point? What does this mean for the series?`;

      return `
    <h3>${esc(g.note || g.name)}</h3>

${scoreboard}

    <p>${angleText}</p>

    <ul class="detail-list">
      <li><span><span class="dl-label">REPLACE — key number:</span>REPLACE: The stat that defined this game. Format: "[Player] did X on Y." Make it specific.</span></li>
      <li><span><span class="dl-label">REPLACE — series situation:</span>REPLACE: Series record, next game date/time/venue. What this result means for momentum.</span></li>
      <li><span><span class="dl-label">How to watch:</span>REPLACE: "Game X · Day · Venue · Time ET · Network."</span></li>
    </ul>

    <div class="angle-box">
      <span class="angle-label">Group Chat Angle</span>
      <p class="angle-text">REPLACE: One specific, inside-knowledge fact about these two teams or players. Something your reader can drop once and sound like they actually know the game.</p>
    </div>`;
    }

    // Additional games: scoreboard + one-line note
    return `
    <h3>${esc(g.note || g.name)}</h3>

${scoreboard}

    <p>REPLACE: 2–3 sentences on this game. Key player, key moment, what's next.</p>`;
  });

  return `  <section class="brief-section" id="sports">
    <div class="section-label">Sports</div>
${gameBlocks.join('\n')}
  </section>`;
}

// ─────────────────────────────────────────────────────────────────────────────
// Markets section — ticker table auto-filled, narrative from AI or REPLACE
// ─────────────────────────────────────────────────────────────────────────────
function buildMarkets({ markets, copy, date }) {
  const openingPara = copy?.marketsTake
    ? `<p>${esc(copy.marketsTake)}</p>`
    : `<p>REPLACE: 2-sentence market take. What's the main story from today's session? What should readers watch next week?</p>`;

  const rows = BRIEF_ROWS.map(row => {
    if (row.type === 'divider') return '        <div class="mkt-div"></div>';

    const sym = row.key;
    const cfg = TICKERS[sym];
    const q = markets?.[sym];

    const price   = q?.price       ? fmtPrice(sym, q.price)       : '[PRICE]';
    const dayPct  = q?.dayChangePct !== undefined ? fmtPct(q.dayChangePct) : '[DAY%]';
    const dayDir  = q?.dayChangePct !== undefined ? (q.dayChangePct >= 0 ? 'up' : 'dn') : '';

    const nameHtml = cfg?.ms
      ? `<a href="https://www.morningstar.com/${cfg.ms}" class="ticker">${esc(cfg.display)}</a>`
      : esc(cfg?.display || sym);

    return `        <div class="mkt-row">
          <div class="m-name">${nameHtml}</div>
          <div class="m-val">${esc(price)}</div>
          <div class="m-day ${dayDir}">${esc(dayPct)}</div>
          <div class="m-wk">[WEEK%]</div><!-- Fill in weekly % manually -->
        </div>`;
  }).join('\n');

  return `  <!-- MARKETS: Prices auto-filled from Finnhub. Fill in [WEEK%] and [PRICE] manually where needed. -->
  <section class="brief-section" id="markets">
    <div class="section-label">Markets</div>

    <h3>REPLACE: Markets headline — one line that captures the week.</h3>

    ${openingPara}

    <p>REPLACE: Second paragraph — one thing that matters next week. Consumer confidence? Fed speak? Earnings? Thin volume?</p>

    <div class="mkt-table">
      <div class="mkt-table-hd">
        <div class="mkt-table-title">Daily Close</div>
        <div class="mkt-table-sub">${esc(date)}</div>
      </div>
      <div class="mkt-table-body">
        <div class="mkt-cols">
          <div class="th">Asset</div>
          <div class="th">Price</div>
          <div class="th">Day</div>
          <div class="th">Week</div>
        </div>
${rows}
      </div>
    </div>

    <ul class="detail-list">
      <li><span><span class="dl-label">Watch next week:</span>REPLACE: The one data point or event that will move markets. Be specific about date and what to expect.</span></li>
      <li><span><span class="dl-label">The one trade worth watching:</span>REPLACE: Pick one ticker showing unusual behavior. What's it doing, and why does it matter?</span></li>
      <li><span><span class="dl-label">What to bring up:</span>REPLACE: One portable market fact — something your reader can say at dinner. Specific number, true, useful.</span></li>
    </ul>
  </section>`;
}

