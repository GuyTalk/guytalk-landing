// ─────────────────────────────────────────────────────────────────────────────
// Sharp Take — closing section, AI-drafted or REPLACE
// ─────────────────────────────────────────────────────────────────────────────
function buildSharpTake({ copy }) {
  const content = copy?.sharpTake
    ? copy.sharpTake.split('\n\n').map(p => `    <p>${esc(p.trim())}</p>`).join('\n')
    : `    <p>REPLACE: First paragraph. The big-picture read on this week. What the sports, markets, and culture stories have in common — or why the combination of them matters this specific weekend.</p>
    <p>REPLACE: Second paragraph. End with an action. "Clear Saturday. If someone asks you to do something else, the answer is no." Make it specific to this issue.</p>`;

  return `  <!-- SHARP TAKE: ${copy?.sharpTake ? 'AI-drafted — review before publishing.' : 'Needs manual copy.'} -->
  <div class="sharp-take">
    <div class="sharp-take-label">The Sharp Take</div>
${content}
  </div>`;
}

// ─────────────────────────────────────────────────────────────────────────────
// Numbers worth stealing — auto-generated where possible, else REPLACE
// ─────────────────────────────────────────────────────────────────────────────
function buildNumbers({ sports, markets, golf }) {
  const items = [];

  // From sports: series record
  if (sports?.length) {
    const g = sports[0];
    const note = g.note || g.shortName;
    items.push({
      num: `${g.home.score}–${g.away.score}`,
      html: `<strong>${esc(note)}.</strong> REPLACE: What this score means in context. What's the stat or historical fact that makes it significant?`,
    });
  }

  // From markets: SPY move
  if (markets?.SPY?.dayChangePct !== undefined && markets.SPY.dayChangePct !== null) {
    const pct = Math.abs(markets.SPY.dayChangePct).toFixed(1);
    const dir = markets.SPY.dayChangePct >= 0 ? 'gain' : 'drop';
    items.push({
      num: `${markets.SPY.dayChangePct >= 0 ? '+' : ''}${markets.SPY.dayChangePct.toFixed(1)}%`,
      html: `<strong>S&amp;P 500 ${dir} today.</strong> REPLACE: What does this number mean in context? Is it bullish? A rebound? Volume situation?`,
    });
  }

  // Golf stat
  if (golf?.leaders?.[0]) {
    items.push({
      num: golf.leaders[0].score,
      html: `<strong>${esc(golf.leaders[0].name)} leading ${esc(golf.name)}.</strong> REPLACE: One fact that makes this leaderboard interesting. Historic performance? Course record pace? Significance for FedEx Cup standings?`,
    });
  }

  // Pad to 3 items
  while (items.length < 3) {
    items.push({
      num: 'REPLACE',
      html: `<strong>REPLACE: Bold context sentence.</strong> REPLACE: 2 sentences explaining why this number matters and what to do with it.`,
    });
  }

  return `  <div class="brief-section">
    <div class="section-label">Numbers worth stealing</div>
    <ul class="numbers-list">
${items.slice(0, 3).map(item => `      <li class="numbers-item">
        <div class="n-num">${esc(item.num)}</div>
        <div>${item.html}</div>
      </li>`).join('\n')}
    </ul>
  </div>`;
}

